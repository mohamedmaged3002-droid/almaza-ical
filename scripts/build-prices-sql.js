// scripts/build-prices-sql.js
// Emit daily nightly rates for the 152 Almaza units into Supabase
// `unit_daily_prices`. Reads output/units/*.json and prices EVERY date in the
// operator's active season via dailyPricesForSeason — the named period covering
// each date, else the operator's "Default Rate" (Lodgify useSmartPricing:false,
// so this is the exact per-night price). Writes output/almaza-prices.sql AND
// output/daily-prices.json (canonical wp -> [{date,price}] map the sheet reuses).
// NO network, NO DB.
//
// The season [SEASON_START, SEASON_END] = the span the operator actually rates
// across the roster (Jun 1 – Oct 31 2026). Default-rate fill is bounded to it;
// dates outside get no row (still BLOCKED — we don't invent off-season prices).
//
// Schema note: currency + source are BOTH NOT NULL on unit_daily_prices — they
// are provided here (EGP / almaza), not omitted.
const fs = require('fs');
const path = require('path');
const { dailyPricesForSeason } = require('../src/lodgify');

const SEASON_START = '2026-06-01';
const SEASON_END = '2026-10-31';

const UNITS_DIR = path.join(__dirname, '..', 'output', 'units');
const OUT = path.join(__dirname, '..', 'output', 'almaza-prices.sql');
const MAP_OUT = path.join(__dirname, '..', 'output', 'daily-prices.json');

function sqlText(v) {
  return `'${String(v).replace(/'/g, "''")}'`;
}

function loadUnits() {
  return fs
    .readdirSync(UNITS_DIR)
    .filter((f) => f.endsWith('.json'))
    .map((f) => JSON.parse(fs.readFileSync(path.join(UNITS_DIR, f), 'utf8')))
    .sort((a, b) => a.wp - b.wp);
}

function main() {
  const units = loadUnits();
  const rows = [];
  const map = {};                                     // wp -> [{date, price}]

  for (const u of units) {
    const daily = dailyPricesForSeason(u.rates || {}, SEASON_START, SEASON_END);
    map[u.wp] = daily;
    for (const { date, price } of daily) {
      // (wp_post_id, date, price, currency, source) — one tuple per line.
      rows.push(`  (${u.wp}, ${sqlText(date)}, ${Number(price)}, 'EGP', 'almaza')`);
    }
  }

  const sql =
    `-- Almaza Bay — daily nightly rates for Supabase \`unit_daily_prices\`\n` +
    `-- Generated by scripts/build-prices-sql.js. Season ${SEASON_START} .. ${SEASON_END}.\n` +
    `-- Every date priced: named period if covered, else the operator's Default Rate.\n` +
    `INSERT INTO unit_daily_prices (wp_post_id, date, price, currency, source) VALUES\n` +
    rows.join(',\n') +
    `\nON CONFLICT (wp_post_id, date) DO UPDATE SET\n` +
    `  price = EXCLUDED.price,\n` +
    `  currency = EXCLUDED.currency,\n` +
    `  source = EXCLUDED.source;\n`;

  fs.writeFileSync(OUT, sql);
  fs.writeFileSync(MAP_OUT, JSON.stringify(map));
  console.log(`Wrote ${OUT} + ${MAP_OUT}`);
  console.log(`Price rows: ${rows.length} (${units.length} units × ${rows.length / units.length} nights)`);
  console.log(`Season: ${SEASON_START} .. ${SEASON_END}`);
}

main();
